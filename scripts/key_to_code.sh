stdin_to_ydotool_codes() {
    grep -o . | while read -r c; do printf '%s ' "${ydotool_keymap["$c"]}"
done
}
multi_key_ydotool() {
    for keys in "$@"; do
        local code= codesl='' codesr=''
        while read -r key; do
            code="${ydotool_keymap["$key"]}"
            codesr="${code##*:1}$codesr"
            codesl="$codesl${code%:1*}:1 "
        done < <(sed 's/\(.\)+/\1\n/' <<< "$keys")
        printf '%s%s ' "$codesl" "$codesr"
    done
}
